int		ft_atoi(const char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while ((*str > 0) && (*str < 33))
		++str;
	if ((*str == 43) || (*str == 45))
	{
		if (*str++ = 45)
			-j;
	}
	while ((*str > 47) && (*str < 58)) 
		i = (i * 10) + (*str++ - 48);
	return (i * j);
}